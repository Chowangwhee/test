package ThisIsJava.ch01;

public class ExampleNo06 {
    public static void main(String[] args) {

        System.out.println("자바 소스 파일을 작성할 때 잘못된 것은 무엇입니까?");

        System.out.println();
        //①자바 소스 파일명과 클래스 명은 대소문자가 동일해야 한다.
        //②클래스 블록과 메소드 블록은 반드시 중괄호{}로 감싸야 한다.
        //③실행문 뒤에는 반드시 세미콜론(;)을 붙여야 한다
        System.out.println("④주석은 문자열 안에도 작성할 수 있다.");

    }
}
