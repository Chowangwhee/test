package ThisIsJava.ch01;

public class ExampleNo02 {
    public static void main(String[] args) {

        System.out.println("Open JDK와 Oracle JDK를 잘못 설명한 것은 무엇입니까?");

        //①둘 다 학습용및 개발용으로는 무료 사용이 가능하다.
        //다만, Oracle JDK는 상업적 사용 시 라이선스 비용이 발생
        System.out.println("②Oracle JDK는 개발 소스 공개 의무가 없지만, Open JDK는 있다.");
        //Open JDK는 오픈 소스 프로젝트로 소스 코드가 공개되어 있다. 반면 Oracle JDK는 상업적 배포판으로 소스 코드 공개 의무가 없다.
        //③둘 다 Java SE의 구현체이다.
        //④JDK 17 LTS 버전의 후속 LTS 버전은 JDK 21이다.
    }
}
